// Helper to convert image URL to a base64 Data URL
const imageUrlToDataUrl = async (url: string): Promise<string> => {
    const response = await fetch(url);
    const blob = await response.blob();
    return new Promise((resolve, reject) => {
      const reader = new FileReader();
      reader.onloadend = () => resolve(reader.result as string);
      reader.onerror = reject;
      reader.readAsDataURL(blob);
    });
};

export const editImage = async (imageUrl: string, prompt: string): Promise<string> => {
  if (!API_KEY) {
    console.warn("API key not set, using placeholder for image edit.");
    // In a real scenario, might return the original image or show an error
    return imageUrl;
  }

  // Ensure image is in base64 format for the API
  let imageDataUrl = imageUrl;
  if (!imageUrl.startsWith('data:')) {
    imageDataUrl = await imageUrlToDataUrl(imageUrl);
  }

  const match = imageDataUrl.match(/^data:(image\/[a-z]+);base64,(.*)$/);
  if (!match) {
    throw new Error('Invalid data URL format for image editing.');
  }
  const [, mimeType, data] = match;

  try {
    const response = await ai.models.generateContent({
      model: 'gemini-2.5-flash-image',
      contents: {
        parts: [
          { inlineData: { mimeType, data } },
          { text: prompt },
        ],
      },
      config: {
        responseModalities: [Modality.IMAGE],
      },
    });

    for (const part of response.candidates[0].content.parts) {
      if (part.inlineData) {
        const base64ImageBytes = part.inlineData.data;
        return `data:${part.inlineData.mimeType};base64,${base64ImageBytes}`;
      }
    }
    throw new Error("No image data in Gemini API response for editing.");
  } catch (error) {
    console.error("Error editing image with Gemini:", error);
    // Fallback to the original image on error
    return imageUrl;
  }
};
