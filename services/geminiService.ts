import { GoogleGenAI, Modality } from "@google/genai";

// FIX: Per coding guidelines, API key must be obtained exclusively from process.env.API_KEY.
const API_KEY = process.env.API_KEY;

if (!API_KEY) {
  // In a real app, you might want to handle this more gracefully.
  // For this environment, we'll throw an error if the key is missing.
  console.warn("API_KEY environment variable not set. Gemini API calls will fail.");
}

const ai = new GoogleGenAI({ apiKey: API_KEY! });

export const generateBadgeImage = async (streak: number): Promise<string> => {
  if (!API_KEY) {
    // Return a placeholder if API key is not available
    console.log("Using placeholder image due to missing API key.");
    return `https://picsum.photos/seed/${streak}/512`;
  }
  
  try {
    const prompt = `A futuristic, cosmic, glowing badge for a ${streak}-day achievement streak. Electric purple, neon pink and cyan colors on a dark background. Epic trophy design, digital art, cinematic lighting.`;

    const response = await ai.models.generateImages({
        model: 'imagen-4.0-generate-001',
        prompt: prompt,
        config: {
          numberOfImages: 1,
          outputMimeType: 'image/png',
          aspectRatio: '1:1',
        },
    });

    if (response.generatedImages && response.generatedImages.length > 0) {
      const base64ImageBytes = response.generatedImages[0].image.imageBytes;
      return `data:image/png;base64,${base64ImageBytes}`;
    } else {
      throw new Error("No image was generated by the API.");
    }
  } catch (error) {
    console.error("Error generating badge image with Gemini:", error);
    // Fallback to a placeholder image on error
    return `https://picsum.photos/seed/${streak}/512`;
  }
};

